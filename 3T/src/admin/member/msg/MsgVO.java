package admin.member.msg;

public class MsgVO {
	private String m_id;
	private String m_msg_content;
	private String m_msg_no;

	public String getM_id() {
		return m_id;
	}

	public void setM_id(String m_id) {
		this.m_id = m_id;
	}

	public String getM_msg_content() {
		return m_msg_content;
	}

	public void setM_msg_content(String m_msg_content) {
		this.m_msg_content = m_msg_content;
	}

	public String getM_msg_no() {
		return m_msg_no;
	}

	public void setM_msg_no(String m_msg_no) {
		this.m_msg_no = m_msg_no;
	}

}
